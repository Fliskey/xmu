#include <stdio.h>

int main(){
	int n;
	scanf("%d",&n);
	double jj = 0;
	if(n > 1000000){
		jj += (n-1000000) * 0.01;
		n = 1000000;
	}
	if(n > 600000){
		jj += (n-600000) * 0.015;
		n = 600000;
	}
	if(n > 400000){
		jj += (n-400000) * 0.03;
		n = 400000;
	}
	if(n > 200000){
		jj += (n-200000) * 0.05;
		n = 200000;
	}
	if(n > 100000){
		jj += (n-100000) * 0.075;
		n = 100000;
	}
	jj += n * 0.1;
	printf("%.0f",jj);	
	return 0;
}
